    <h2>А что вообще такое авторское право?</h2>
    <p>Авторское право – это несколько прав в одном, а именно:<br><br>
- исключительное право на произведение (аналог права собственности);<br>
- право называться автором и право на указание своего имени как автора;<br>
- право на неприкосновенность произведения;<br>
- право на обнародование произведения.<br><br>
Это основные права. Бывают еще дополнительные, которые возникают в зависимости от ситуации.</p>
